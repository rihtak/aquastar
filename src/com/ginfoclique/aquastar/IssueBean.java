package com.ginfoclique.aquastar;

public class IssueBean {
	private int id;
	private String name;
	private String date_details;
	private String status;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDateDetails() {
		return date_details;
	}
	public void setDateDetails(String date_detials) {
		this.date_details = date_detials;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status_i) {
		this.status = status_i;
	}

	
}
